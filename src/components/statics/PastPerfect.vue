    <div class="subtitle">Употребление Past Perfect</div>
    <div class="text">Давайте узнаем, когда употребляется Past Perfect:</div>
    <div class="text">
      1. Past Perfect используется, когда у нас есть два действия в прошлом и
      нам важно показать, что одно из них произошло раньше другого. Событие,
      которое произошло раньше, описываем с помощью Past Perfect, а второе
      действие — с помощью Past Simple.

      <ul class="list">
        <li class="list_item">
          She came to the office to meet him, but he had already left. — Она

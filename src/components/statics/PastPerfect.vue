        believe (верить, полагать), hope (надеяться), remember (помнить), forget
        (забывать) и др. Эти глаголы употребляем в Past Simple, а вторую часть
        предложения — в Past Perfect. Например:

        <ul class="list">
          <li class="list_item">
            I thought you had called Eric to invite him to the party. — Я

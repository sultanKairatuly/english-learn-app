    <div class="subtitle">Что такое Present Perfect Tense?</div>
    <div class="text">
      Present Perfect Tense (Present Perfect) — это настоящее совершенное время
      в английском языке. Оно обозначает действие, которое завершилось в
      настоящий момент времени.<br />

      В этом и состоит основная сложность времени Present Perfect для изучающих.
      В русском языке нет времени аналогичного Present Perfect. Для нас если
      что-то происходит сейчас — это и есть настоящее, а если совершилось — это
      уже прошлое.<br />

      Но не для англичан. Они воспринимают время немного по-другому. По логике
      носителей языка, действие вполне может закончиться и в настоящем или
      близко к настоящему моменту. Для выражения такой связи прошлого с
      настоящим и существует Present Perfect.<br />

      Из-за этих особенностей в понимании действий и времени — на русский язык
      Present Perfect обычно переводится глаголом в прошедшем времени.<br />

      I have already done my homework — Я уже сделал домашнее задание<br />

      В этом примере используется время Present Perfect (have done), потому что
      речь идет о том, что действие (работа над домашним заданием) закончилось
      совсем недавно.<br />

      Но на русский язык мы переводим предложение используя прошедшее время (уже
      сделал).<br />
    </div>
    <div class="subtitle">Как образуется Present Perfect?</div>
    <div class="text">
      Время Present Perfect образуется при помощи вспомогательного глагола have
      / has и Past Participle (третьей формы смыслового глагола: V3).

      <ul class="list">
        <li class="list_item">
          >I / You / We / They → have (для 1-го, 2-го лица и форм множественного
          числа)
        </li>
        <li class="list_item">
          She / He / It → has (для 3-го лица единственного числа)
        </li>
      </ul>
      Завершает конструкцию времени Present Perfect смысловой глагол в третьей
      форме (V3).<br />
    </div>
    <div class="text">
      Если смысловой глагол правильной формы — то его третья форма (V3)

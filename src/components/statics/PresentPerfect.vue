      для правильных глаголов или третьей формы неправильного глагола (V3) по
      формуле:
      <ul class="list">
        <li class="list_item">I / You / We / They + have + Ved (V3)</li>
        <li class="list_item">She / He / It + has + Ved (V3)</li>
        <li class="list_item">I have decided — Я решил</li>
        <li class="list_item">You have played — Ты играл</li>
        <li class="list_item">He has done — Он сделал</li>

    <div class="subtitle">Случаи употребления Present Continuous</div>
    <div class="text">
      1. Время Present Continuous используется для описания действия, которое
      происходит в текущий момент времени:

      <ul class="list">
        <li class="list_item">
          Listen! The music is playing. — Слушай! Музыка играет.
        </li>
        <li class="list_item">
          Hush! The kids are sleeping. — Ш-ш! Дети спят.
        </li>
      </ul>
      В такой ситуации часто встречаются слова-маркеры: now (сейчас), right now
      (в эту самую минуту), at the moment (в данный момент), at present (в
      настоящее время). Это своего рода указатели на настоящее длительное время.
      <li class="list_item">
        Steve is watching TV at the moment. — Сейчас Стив смотрит телевизор.
      </li>
    </div>
    <div class="text">
      2. Present Continuous используется для описания длительных действий в
      настоящем, даже если они не происходят в момент речи. То есть на
      протяжении какого-то времени мы периодически выполняем это действие:
      <li class="list_item">
        Jessica is learning French. — Джессика учит французский. (она занимается
        изучением французского уже какое-то время и будет продолжать учить его,
        но она не занята изучением французского прямо сейчас)
      </li>
      В этой ситуации также часто встречаются такие слова-маркеры Present
      Continuous, как now, these days, currently. Они переводятся «сейчас», «на
      текущий момент».
      <li class="list_item">
        Pete is currently training to participate in the Olympics. — Пит сейчас
        тренируется для участия в Олимпийский играх. (он сейчас ходит на
        тренировки и будет продолжать их до Олимпийских игр, но прямо сейчас он
        не на тренировке)
      </li>
    </div>
    <div class="text">
      3. Present Continuous используется для описания изменений, происходящих в
      какой-то ситуации, и процесса развития. В таких конструкциях действие
      часто передается глаголами to get (становиться), to increase
      (увеличивать/увеличиваться), to decrease (уменьшать/уменьшаться), to rise
      (подниматься), to fall (падать), to start (начинать/начинаться), to change
      (менять/меняться) и т. д.

      <ul class="list">
        <li class="list_item">
          An average day temperature is slowly increasing. — Средняя температура
          постепенно увеличивается.
        </li>
        <li class="list_item">
          The petrol is getting cheaper day by day. — С каждым днем топливо
          дешевеет.
        </li>
      </ul>
    </div>
    <div class="text">
      4. Present Continuous может использоваться для обозначения событий
      ближайшего будущего, но только в том случае, если это касается
      запланированных действий. На будущее в подобных предложениях указывают
      время, дни недели или слова tomorrow (завтра), next week (на следующей
      неделе), next month (в следующем месяце), next year (в следующем году).

      <ul class="list">
        <li class="list_item">
          Cris is buying a new phone tomorrow. — Завтра Крис купит новый
          телефон. (он уже выбрал модель, отложил на телефон деньги)
        </li>
        <li class="list_item">
          We are flying to India next week. — На следующей неделе мы летим в
          Индию. (поездка уже запланирована, билеты куплены, путешествие точно
          состоится)
        </li>
      </ul>
      Говоря о ближайшем будущем, мы зачастую используем глаголы движения: to go
      (идти), to leave (уходить), to come (приходить). Эти глаголы показывают,
      что действие будет произведено незамедлительно или в ближайшем будущем.

      <ul class="list">
        <li class="list_item">
          I'm coming in 5 minutes. Can you meet me up? — Я приду через 5 минут.
          Встретишь меня? (я уже в пути и вот-вот приду.)
        </li>
        <li class="list_item">
          I don’t feel well. I am going to bed. — Я нехорошо себя чувствую. Я
          иду спать. (я совершу это действие незамедлительно)
        </li>
      </ul>
    </div>
    <div class="text">
      5. Present Continuous помогает выразить недовольство или раздражение при
      употреблении его со словами constantly (постоянно), all the time (все
      время), always (всегда). Так мы показываем, что какое-то регулярное или
      часто происходящее действие вызывает отрицательные эмоции.

      <ul class="list">
        <div class="list_item">
          Susan is always leaving her coffee mug in the sink. — Сьюзан постоянно
          оставляет свою чашку для кофе в раковине. (действие совершается
          регулярно, и говорящий недоволен этой ситуацией)
        </div>
      </ul>
    </div>


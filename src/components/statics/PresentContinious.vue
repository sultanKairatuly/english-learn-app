    <div class="subtitle">Как образуется время Present Continuous</div>
    <div class="text">
      Чтобы не ошибиться в написании глагола с окончанием -ing, обратите
      внимание на следующие правила:

      <ul class="list">
        <li class="list_item">
          если глагол оканчивается на гласную -e, то она опускается: bake –
          baking;
        </li>
        <li class="list_item">
          когда глагол оканчивается на гласные -ie, они меняются на -y: tie –
          tying;
        </li>
        <li class="list_item">
          если глагол оканчивается на одну согласную букву, перед которой есть
          ударный гласный звук, то согласная буква на конце удваивается: let –
          letting, begin – beginning, regret – regretting.
        </li>
      </ul>
      <div class="text">Утвердительное предложение:</div>
      <li class="list_item">
        She is petting her dog now. — Она сейчас гладит свою собаку.
      </li>
      <div class="text">
        В отрицательном предложении между вспомогательным и основным глаголами
        ставится частица not.
      </div>
      <li class="list_item">
        She is not (isn’t) petting her dog now. — Она сейчас не гладит свою
